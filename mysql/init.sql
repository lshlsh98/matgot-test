`member_pw`       CHAR(60)          NOT NULL,
`member_name`     VARCHAR(30)       NOT NULL,
`member_email`    VARCHAR(50)       NOT NULL,
`member_nickname` VARCHAR(20)       NOT NULL,
`member_thumb`    VARCHAR(255)      NULL,
`member_address`  VARCHAR(300)  NULL,
