-- 4. 기타 회원 관련 테이블 생성
CREATE TABLE `NATIVES_TBL` (
`native_no`       BIGINT       NOT NULL AUTO_INCREMENT,
`member_id`       VARCHAR(20)  NULL,
`region`          VARCHAR(50)  NOT NULL COMMENT '시도 시군구 동면읍',
`native_deadline` DATETIME     NULL COMMENT 'NULL이면 제한 없음',
PRIMARY KEY (`native_no`),
INDEX `idx_member_id` (`member_id`),
CONSTRAINT `fk_natives_member`
FOREIGN KEY (`member_id`)
   REFERENCES `MEMBER_TBL` (`member_id`)
   ON DELETE SET NULL
   ON UPDATE CASCADE
);

CREATE TABLE `LOGIN_LOG_TBL` (
`login_no`      BIGINT           NOT NULL AUTO_INCREMENT,
`member_no`     BIGINT           NULL,
`login_status`  TINYINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '0:로그아웃/1:로그인',
`login_time`    DATETIME         NOT NULL DEFAULT CURRENT_TIMESTAMP,
PRIMARY KEY (`login_no`),
INDEX `idx_member_no` (`member_no`),
CONSTRAINT `fk_login_member`
 FOREIGN KEY (`member_no`)
     REFERENCES `MEMBER_TBL` (`member_no`)
     ON DELETE SET NULL
     ON UPDATE CASCADE
);

CREATE TABLE `SOCIAL_LOGIN_TBL` (
`social_no`     BIGINT    NOT NULL AUTO_INCREMENT,
`member_no`     BIGINT    NULL,
`social_google` DATETIME  NULL,
`social_kakao`  DATETIME  NULL,
`social_naver`  DATETIME  NULL,
`social_apple`  DATETIME  NULL,
PRIMARY KEY (`social_no`),
INDEX `idx_member_no` (`member_no`),
CONSTRAINT `fk_social_member`
    FOREIGN KEY (`member_no`)
        REFERENCES `MEMBER_TBL` (`member_no`)
        ON DELETE SET NULL
        ON UPDATE CASCADE
);

-- 5. 맛집 관련 테이블 생성
CREATE TABLE restaurant_tbl (
rest_no BIGINT AUTO_INCREMENT PRIMARY KEY,
member_id VARCHAR(50) NOT NULL,
rest_name VARCHAR(100) NOT NULL,
rest_addr VARCHAR(255) NOT NULL,
lat DOUBLE NOT NULL,
lng DOUBLE NOT NULL,
category VARCHAR(50) NOT NULL,
phone VARCHAR(20),
hours VARCHAR(100),
rating_sum INT NOT NULL DEFAULT 0,
review_total_count INT NOT NULL DEFAULT 0,
local_review_count INT NOT NULL DEFAULT 0,
rest_content TEXT NOT NULL,
ai_review TEXT,
rest_status VARCHAR(20) NOT NULL DEFAULT 'NORMAL',
rest_thumb varchar(300),
created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
CONSTRAINT uk_same_restaurant UNIQUE (rest_name, rest_addr),
CONSTRAINT fk_restaurant_writer FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE
);

CREATE TABLE rest_report_tbl (
rest_rp_no BIGINT AUTO_INCREMENT PRIMARY KEY,
member_id VARCHAR(50) NOT NULL,
rest_no BIGINT NOT NULL,
reason VARCHAR(50) NOT NULL CHECK (reason IN ('허위정보', '욕설비방', '광고스팸', '기타')),
detail TEXT,
report_status TINYINT NOT NULL DEFAULT 0 CHECK (report_status IN (0, 1, 2)),
report_date DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
CONSTRAINT fk_rest_report_member FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE,
CONSTRAINT fk_report_restaurant FOREIGN KEY (restaurant_tbl) REFERENCES restaurant_tbl(rest_no) ON DELETE CASCADE,
CONSTRAINT uk_report_unique_rest UNIQUE (member_id, rest_no)
);

CREATE TABLE rest_like_tbl (
member_id VARCHAR(50) NOT NULL,
rest_no BIGINT NOT NULL,
CONSTRAINT pk_like_rest PRIMARY KEY (member_id, rest_no),
CONSTRAINT fk_like_member FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE,
CONSTRAINT fk_like_restaurant FOREIGN KEY (rest_no) REFERENCES restaurant_tbl(rest_no) ON DELETE CASCADE
);

CREATE TABLE review_tbl (
review_no BIGINT AUTO_INCREMENT PRIMARY KEY,
member_id VARCHAR(50) NOT NULL,
rest_no BIGINT NOT NULL,
review_content TEXT NOT NULL,
rating TINYINT UNSIGNED NOT NULL CHECK (rating BETWEEN 1 AND 5),
visit_date DATE NOT NULL,
is_local_review BOOLEAN NOT NULL DEFAULT FALSE,
review_status TINYINT NOT NULL DEFAULT 0,
created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
CONSTRAINT fk_review_member FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE,
CONSTRAINT fk_review_restaurant FOREIGN KEY (rest_no) REFERENCES restaurant_tbl(rest_no) ON DELETE CASCADE
);

CREATE TABLE review_menu_tbl (
review_menu_no BIGINT AUTO_INCREMENT PRIMARY KEY,
review_no BIGINT NOT NULL,
menu_name VARCHAR(100) NOT NULL,
CONSTRAINT uk_review_menu UNIQUE (review_no, menu_name),
CONSTRAINT fk_review_menu_review FOREIGN KEY (review_no) REFERENCES review_tbl(review_no) ON DELETE CASCADE
);

CREATE TABLE review_tags_tbl(
review_tags_no BIGINT AUTO_INCREMENT PRIMARY KEY,
review_no BIGINT NOT NULL,
tag_name VARCHAR(30) NOT NULL,
CONSTRAINT uq_review_tag UNIQUE (review_no, tag_name),
CONSTRAINT fk_review_tags_tbl_review FOREIGN KEY (review_no) REFERENCES review_tbl(review_no) ON DELETE CASCADE
);

CREATE TABLE review_images_tbl (
review_image_no BIGINT AUTO_INCREMENT PRIMARY KEY,
review_no BIGINT NOT NULL,
image_url VARCHAR(500) NOT NULL,
CONSTRAINT fk_review_image FOREIGN KEY (review_no) REFERENCES review_tbl(review_no) ON DELETE CASCADE
);

CREATE TABLE review_like_tbl(
review_no BIGINT NOT NULL,
member_id VARCHAR(50) NOT NULL,
CONSTRAINT pk_like_review PRIMARY KEY (review_no, member_id),
CONSTRAINT fk_review_like_member FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE,
CONSTRAINT fk_review_like_review FOREIGN KEY (review_no) REFERENCES review_tbl(review_no) ON DELETE CASCADE
);

CREATE TABLE review_report_tbl (
review_rp_no BIGINT AUTO_INCREMENT PRIMARY KEY,
member_id VARCHAR(50) NOT NULL,
review_no BIGINT NOT NULL,
reason VARCHAR(50) NOT NULL CHECK (reason IN ('허위정보', '욕설비방', '광고스팸', '기타')),
detail TEXT,
created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
report_status TINYINT NOT NULL DEFAULT 0 CHECK (report_status IN (0, 1, 2)),
CONSTRAINT fk_review_report_review FOREIGN KEY (review_no) REFERENCES review_tbl(review_no) ON DELETE CASCADE,
CONSTRAINT fk_review_report_member FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE,
CONSTRAINT uk_review_report UNIQUE (member_id, review_no)
);

CREATE TABLE review_comment_tbl (
comment_no BIGINT AUTO_INCREMENT PRIMARY KEY,
review_no BIGINT NOT NULL,
member_id VARCHAR(50) NOT NULL,
parent_comment BIGINT,
depth TINYINT UNSIGNED NOT NULL DEFAULT 0 CHECK (depth IN (0,1)),
content TEXT NOT NULL,
com_status TINYINT UNSIGNED NOT NULL DEFAULT 0,
created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
CONSTRAINT fk_review_comment_review FOREIGN KEY (review_no) REFERENCES review_tbl(review_no) ON DELETE CASCADE,
CONSTRAINT fk_review_comment_member FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE,
CONSTRAINT fk_review_comment_parent FOREIGN KEY (parent_comment) REFERENCES review_comment_tbl(comment_no) ON DELETE CASCADE
);

CREATE TABLE review_comment_report_tbl (
review_com_rp_no BIGINT AUTO_INCREMENT PRIMARY KEY,
member_id VARCHAR(50) NOT NULL,
comment_no BIGINT NOT NULL,
reason VARCHAR(50) NOT NULL CHECK (reason IN ('허위정보', '욕설비방', '광고스팸', '기타')),
detail TEXT,
created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
report_status TINYINT NOT NULL DEFAULT 0 CHECK (report_status IN (0, 1, 2)),
       CONSTRAINT fk_report_com_member FOREIGN KEY (member_id) REFERENCES MEMBER_TBL(member_id) ON DELETE CASCADE,
       CONSTRAINT fk_report_com_comment FOREIGN KEY (comment_no) REFERENCES review_comment_tbl(comment_no) ON DELETE CASCADE,
       CONSTRAINT uk_report_unique_comment UNIQUE (member_id, comment_no)
);

